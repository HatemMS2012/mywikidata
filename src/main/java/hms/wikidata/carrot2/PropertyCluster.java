package hms.wikidata.carrot2;

import java.util.List;

public class PropertyCluster {

	
	private String id ;
	private List<String> clusterLables ;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public List<String> getClusterLables() {
		return clusterLables;
	}
	public void setClusterLables(List<String> clusterLables) {
		this.clusterLables = clusterLables;
	}
	
	
}
