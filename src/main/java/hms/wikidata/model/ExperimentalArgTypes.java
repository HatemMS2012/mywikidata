package hms.wikidata.model;

public class ExperimentalArgTypes {

	private String id;
	private String label;
	private String description;
	private String category;
	
	private String typeArg1;
	private String typeArg2;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getTypeArg1() {
		return typeArg1;
	}
	public void setTypeArg1(String typeArg1) {
		this.typeArg1 = typeArg1;
	}
	public String getTypeArg2() {
		return typeArg2;
	}
	public void setTypeArg2(String typeArg2) {
		this.typeArg2 = typeArg2;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	
	
	
}
